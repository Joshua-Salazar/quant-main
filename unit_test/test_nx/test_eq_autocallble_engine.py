    def build_eq_vol_grid(self, undID):
        vols = VolSliceMap()
        for i in range(len(self.expiryDate[undID])):
            slice = Slice()
            expiry = self.expiryDate[undID][i]
            for j in range(len(self.strikes[undID])):
                strike = Strike(self.strikes[undID][j])
                vol = self.volByStrikeExpiry[undID][i][j]
                slice[strike] = vol

            vols[expiry] = slice

        grid = EqVolatilityGrid.make(undID, self.dc, self.valuationTimePoint, vols, self.undNames[undID],
                                     self.undCcys[undID], self.undExchCodes[undID])

        return grid

